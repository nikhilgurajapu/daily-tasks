"""1.Write a Python program to sort a list of 10 random integers in ascending and descending order using sort() and reverse().
"""
list_1 = [1,7,3,6,2,0,8,9,6,5]

list_1.sort(reverse=True)
print(list_1)

list_1.sort()
print(list_1)

print("---------------------------")

list_1.reverse()
print(list_1)
